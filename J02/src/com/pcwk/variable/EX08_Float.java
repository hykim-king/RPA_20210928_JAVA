public class EX08_Float {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		double dNum = 3.14;//8byte 실수의 default value는 double
		float  fNum = 3.14F;//4byte -> F/f 
		
		System.out.println("dNum:"+dNum);//dNum:3.14
		System.out.println("fNum:"+fNum);//fNum:3.14

	}

}
